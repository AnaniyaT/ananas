class Solution:
    def isPowerOfThree(self, n: int) -> bool:
        mySet = {1, 3, 9, 2187, 3486784401, 31381059609, 27, 6561, 59049, 10460353203, 43046721, 129140163, 387420489, 94143178827, 81, 1594323, 729, 1162261467, 282429536481, 19683, 14348907, 531441, 243, 4782969, 177147}

        if n in mySet:
            return True
        else:
            return False